// print zero to ten using while loop

var numbers = 1;
while (numbers <= 10) {
    console.log(numbers);
    numbers++;
}

// print only even num from zero to twenty only using while loop

var numbers = 0;
while (numbers <= 20) {
    console.log(numbers);
    numbers = numbers + 2;
}